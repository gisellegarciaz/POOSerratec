
public class TabelaMultipl {
	public static void main(String[] args) {
        int numero = 2;

        System.out.println("Tabela de Multiplicação para o número " + numero + ":");
        for (int i = 1; i <= 10; i++) {
            int resultado = numero * i;
            System.out.println(numero + " x " + i + " = " + resultado);
        }
    }
	
}
